int	ft_nbrlen(long nb)
{
	int	i;

	i = 1;
	if (nb < 0)
	{
		i++;
		nb = -nb;
	}
	while (nb >= 10)
	{
		i++;
		nb /= 10;
	}
	return (i);
}
